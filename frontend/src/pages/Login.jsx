/** Ensure a phone number has the country code prefix (defaults +91 India). */
const normalisePhone = (phone) => {
    const clean = phone.replace(/\s+/g, '');
    if (clean.startsWith('+')) return clean;
    if (clean.startsWith('0')) return `+91${clean.slice(1)}`;
    return `+91${clean}`;
};

